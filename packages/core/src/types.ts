/**
 * A discrete event emitted during streaming generation.
 *
 * - `text`        ‚Äî incremental text delta
 * - `reasoning`   ‚Äî incremental reasoning / thinking delta
 * - `tool_use`    ‚Äî the model has begun or completed a tool-use block
 * - `tool_result` ‚Äî a tool result has been appended to the stream
 * - `budget_exceeded` ‚Äî token budget threshold reached for this run
 * - `done`        ‚Äî the stream has ended; `data` is the final {@link LLMResponse}
 * - `error`       ‚Äî an unrecoverable error occurred; `data` is an `Error`
 */
export interface StreamEvent {
  readonly type: 'text' | 'reasoning' | 'tool_use' | 'tool_result' | 'loop_detected' | 'budget_exceeded' | 'done' | 'error'
  readonly data: unknown
  /**
   * Normalized failure metadata for an `error` event when its source is known.
   * This lets orchestrators distinguish a provider failure from a callback or
   * framework failure without inferring the source from the error text.
   */
  readonly errorInfo?: StructuredTraceError
}

