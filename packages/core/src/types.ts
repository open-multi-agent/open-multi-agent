/** The serialisable tool schema sent to the LLM provider. */
export interface LLMToolDef {
  readonly name: string
  readonly description: string
  /** JSON Schema object describing the tool's `input` parameter. */
  readonly inputSchema: Record<string, unknown>
}

/**
 * Context injected into every tool execution.
 *
 * Both `abortSignal` and `abortController` are provided so that tools and the
 * executor can choose the most ergonomic API for their use-case:
 *
 * - Long-running shell commands that need to kill a child process can use
 *   `abortController.signal` directly.
 * - Simple cancellation checks can read `abortSignal?.aborted`.
 *
 * When constructing a context, set `abortController` and derive `abortSignal`
 * from it, or provide both independently.
 */
export interface ToolUseContext {
  /** High-level description of the agent invoking this tool. */
  readonly agent: AgentInfo
  /** Team context, present when the tool runs inside a multi-agent team. */
  readonly team?: TeamInfo
  /**
   * Convenience reference to the abort signal.
   * Equivalent to `abortController?.signal` when an `abortController` is set.
   */
  readonly abortSignal?: AbortSignal
  /**
   * Full abort controller, available when the caller needs to inspect or
   * programmatically abort the signal.
   * Tools should prefer `abortSignal` for simple cancellation checks.
   */
  readonly abortController?: AbortController
  /**
   * Working directory for filesystem-tool sandboxing.
   *
   * - `string` ‚Äî built-in filesystem tools (`file_read`, `file_write`,
   *   `file_edit`, `grep`, `glob`) require absolute paths and reject paths
   *   that resolve outside this directory (including via symlinks).
   * - `null` ‚Äî sandbox is explicitly disabled; tools accept arbitrary paths.
   * - `undefined` ‚Äî falls back to `<process.cwd()>/.agent-workspace`, an
   *   auto-created sandbox subdirectory (sandbox enabled).
   */
  readonly cwd?: string | null
  /** Arbitrary caller-supplied metadata (session ID, request ID, etc.). */
  readonly metadata?: Readonly<Record<string, unknown>>
  /** Run ID associated with this tool call, when available. */
  readonly runId?: string
  /** Task ID associated with this tool call, when available. */
  readonly taskId?: string
  /**
   * Per-agent scoped secrets for tool code to consume (API tokens, service
   * keys, etc.), sourced from {@link AgentConfig.credentials}. A tool reads
   * `context.credentials?.STRIPE_KEY` instead of closing over a module-level
   * secret, so each agent holds only the credentials it was assigned ‚Äî a
   * compromised or misbehaving subagent cannot reach another agent's secrets.
   *
   * This is an ergonomic scoping seam, not an isolation boundary: tool code
   * still runs in-process and can read `process.env`. Values are treated as
   * secrets ‚Äî the `credentials` key is auto-redacted from traces and
   * dashboards (see `utils/redaction.ts`).
   */
  readonly credentials?: Readonly<Record<string, string>>
}

