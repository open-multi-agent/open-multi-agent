/**
 * Extended thinking / reasoning configuration shared by {@link AgentConfig}
 * and {@link LLMChatOptions}.
 *
 * - `budgetTokens` maps to Anthropic's `thinking.budget_tokens` and Gemini's
 *   `thinkingConfig.thinkingBudget`.
 * - `effort` maps to OpenAI o-series / gpt-5 `reasoning_effort`. Carried as
 *   an explicit value rather than derived from `budgetTokens` so the call
 *   site stays unambiguous across providers.
 *
 * The `effort` union is intentionally narrowed to the values declared by
 * the pinned `openai` SDK (`'low' | 'medium' | 'high'`). Newer values
 * shipped by the API but not yet in the SDK type union (e.g. gpt-5's
 * `'minimal'`, GPT-5.5's `'none'`) should be passed via `extraBody:
 * { reasoning_effort: '<value>' }`, matching how `top_k` / `min_p` are
 * handled for vLLM.
 *
 * Adapters that don't recognise a given field ignore it.
 */
export interface ThinkingConfig {
  readonly enabled: boolean
  readonly budgetTokens?: number
  readonly effort?: 'low' | 'medium' | 'high'
}

/** Context passed to the {@link AgentConfig.beforeRun} hook. */
export interface BeforeRunHookContext {
  /** The user prompt text. */
  readonly prompt: string
  /** The agent's static configuration. */
  readonly agent: AgentConfig
}

/**
 * A minimal, SDK-agnostic view of an ACP `session/request_permission` prompt,
 * passed to a {@link AcpAgentBackendConfig.permission} callback so callers can decide
 * without importing `@agentclientprotocol/sdk`.
 */
export interface AcpPermissionRequest {
  /** Human-readable title of the pending tool call (e.g. `"Edit src/app.ts"`). */
  readonly title: string
  /** ACP tool kind when the agent provides one (e.g. `'edit'`, `'execute'`, `'read'`). */
  readonly kind?: string
  /** The option kinds the agent offered (e.g. `'allow_once'`, `'reject_always'`). */
  readonly optionKinds: readonly string[]
}

/**
 * How an ACP-backed agent answers a permission prompt. OMA runs agents
 * autonomously inside a task DAG, so the default is `'auto-approve'`.
 *  - `'auto-approve'` ‚Äî select an `allow_*` option, preferring the least-privilege
 *    `allow_once` over a session-wide `allow_always` (falls back to cancel).
 *  - `'reject'` ‚Äî select a `reject_*` option, preferring `reject_once` (falls back to cancel).
 *  - function ‚Äî decide per request; `true` approves, `false` rejects.
 */
export type AcpPermissionPolicy =
  | 'auto-approve'
  | 'reject'
  | ((request: AcpPermissionRequest) => boolean | Promise<boolean>)

/**
 * Configuration for running an external agent over the Agent Client Protocol
 * (ACP) as an OMA team member ‚Äî see {@link AgentConfig.backend}. The agent is a
 * local subprocess (a coding CLI such as Gemini CLI or Claude Code) that runs its
 * own agentic loop, while OMA drives it, collects its output and token usage, and
 * schedules it in the task DAG alongside LLM agents.
 *
 * Requires the optional peer `@agentclientprotocol/sdk`.
 */
export interface AgentBackendConfig {
  /** Backend discriminant. */
  readonly kind: 'acp'
  /** Executable to spawn (e.g. `'npx'`, `'gemini'`, `'codex-acp'`). */
  readonly command: string
  /** Arguments passed to `command` (e.g. `['-y', '@agentclientprotocol/claude-agent-acp']`). */
  readonly args?: readonly string[]
  /** Extra environment variables for the subprocess, merged over `process.env`. */
  readonly env?: Readonly<Record<string, string>>
  /**
   * Working directory the agent reads and edits. Defaults to `process.cwd()`.
   * Unlike OMA's filesystem-tool sandbox, an ACP agent accesses this directory
   * directly ‚Äî scope it to a project you trust the external agent with.
   */
  readonly cwd?: string
  /** How to answer the agent's permission prompts. Defaults to `'auto-approve'`. */
  readonly permission?: AcpPermissionPolicy
}

/** Alias for the ACP backend config; `AgentBackendConfig` is kept for v1.10 compatibility. */
export interface AcpAgentBackendConfig extends AgentBackendConfig {}

/** How a generic process backend receives a prompt. */
export type ProcessBackendInputMode = 'stdin' | 'argument' | 'none'

/**
 * Configuration for running a generic local process as an OMA team member.
 *
 * Unlike ACP, this backend does not speak an agent protocol. It starts a fresh
 * process per run, sends the prompt by stdin or final argument, maps stdout to
 * the agent output, and treats non-zero exits as task failures.
 */
export interface ProcessAgentBackendConfig {
  /** Backend discriminant. */
  readonly kind: 'process'
  /** Executable to spawn (e.g. `'node'`, `'python'`, `'my-cli'`). */
  readonly command: string
  /** Arguments passed to `command` before the prompt argument, if any. */
  readonly args?: readonly string[]
  /** Extra environment variables for the subprocess, merged over `process.env`. */
  readonly env?: Readonly<Record<string, string>>
  /** Working directory for the subprocess. Defaults to `process.cwd()`. */
  readonly cwd?: string
  /**
   * Prompt delivery mode. Defaults to `'stdin'`.
   * - `'stdin'`: write the prompt to stdin and close it.
   * - `'argument'`: append the prompt as the final command argument.
   * - `'none'`: do not send the prompt; useful for fixed command adapters.
   */
  readonly input?: ProcessBackendInputMode
}

/** External backend configuration keyed by `kind`. */
export type ExternalAgentBackendConfig = AgentBackendConfig | ProcessAgentBackendConfig

/** Static configuration for a single agent. */
export interface AgentConfig {
  readonly name: string
  /**
   * Model identifier (e.g. `'claude-opus-4-6'`).
   *
   * Optional in an orchestrated run: under `runTeam` / `runTasks` / `runAgent`
   * an unset model inherits {@link OrchestratorConfig.defaultModel} (mirroring
   * how `provider` / `baseURL` / `apiKey` inherit their `default*` siblings).
   * A standalone `new Agent(...)` has no orchestrator to inherit from, so it
   * must set this explicitly ‚Äî the constructor throws when it is absent.
   */
  readonly model?: string
  /**
   * Optional custom {@link LLMAdapter}. When set, the agent uses this adapter
   * directly and skips {@link createAdapter} ‚Äî `provider`, `apiKey`, `baseURL`,
   * and `region` are ignored for LLM calls.
   */
  readonly adapter?: LLMAdapter
  /**
   * Run this agent on an external {@link ExternalAgentBackendConfig} instead of
   * an LLM adapter. When set, the
   * LLM-specific fields (`model`, `provider`, `adapter`, sampling, tools, context
   * strategy) do not apply ‚Äî the external agent runs its own loop ‚Äî but the agent
   * still participates in the task DAG, shared memory, cascade-on-failure, and
   * token budget like any other team member. ACP backends require the optional
   * peer `@agentclientprotocol/sdk`; generic process backends use Node child
   * processes and do not require an optional peer.
   */
  readonly backend?: ExternalAgentBackendConfig
  readonly provider?: SupportedProvider
  /**
   * Custom base URL for OpenAI-compatible APIs (Ollama, vLLM, LM Studio, etc.).
   * Note: local servers that don't require auth still need `apiKey` set to a
   * non-empty placeholder (e.g. `'ollama'`) because the OpenAI SDK validates it.
   */
  readonly baseURL?: string
  /** API key override; falls back to the provider's standard env var. */
  readonly apiKey?: string
  /** AWS region override for the `bedrock` provider; falls back to `AWS_REGION` env var, then `'us-east-1'`. Ignored by all other providers. */
  readonly region?: string
  readonly systemPrompt?: string
  /**
   * Custom tool definitions to register alongside built-in tools.
   * Created via `defineTool()`. Custom tools bypass `tools` (allowlist)
   * and `toolPreset` filtering, but can still be blocked by `disallowedTools`.
   *
   * Tool names must not collide with built-in tool names; a duplicate name
   * will throw at registration time.
   */
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  readonly customTools?: readonly ToolDefinition<any>[]
  /** Names of tools (from the tool registry) available to this agent. */
  readonly tools?: readonly string[]
  /** Names of tools explicitly disallowed for this agent. */
  readonly disallowedTools?: readonly string[]
  /**
   * Optional per-call tool gate. Called after tool input validates and before
   * execution. Return `{ action: 'deny' }` to surface an error ToolResult to the
   * model without throwing or invoking the tool implementation.
   */
  readonly onToolCall?: ToolCallGate
  /** Predefined tool preset for common use cases. */
  readonly toolPreset?: 'readonly' | 'readwrite' | 'full'
  /**
   * Per-agent scoped secrets made available to tool code via
   * {@link ToolUseContext.credentials}. Give each agent only the credentials
   * it needs (e.g. a `researcher` gets a search-API key, a `publisher` gets a
   * CMS token) instead of closing a single shared secret over every tool.
   * Never merged across agents and never inherited from the coordinator: an
   * agent holds exactly the bag set here, or none.
   *
   * A scoping convenience, not a sandbox ‚Äî tool code runs in-process and can
   * still read `process.env`. Values are treated as secrets: the `credentials`
   * key is auto-redacted from traces and dashboards.
   */
  readonly credentials?: Readonly<Record<string, string>>
  /**
   * Root directory used by built-in filesystem tools (`file_read`,
   * `file_write`, `file_edit`, `grep`, `glob`). Paths must be absolute and
   * resolve inside this directory; symlinks are resolved before the check.
   *
   * Defaults to {@link OrchestratorConfig.defaultCwd} (or
   * `<process.cwd()>/.agent-workspace` if neither is set). Pass `null` to
   * disable the sandbox for this agent.
   */
  readonly cwd?: string | null
  readonly maxTurns?: number
  readonly maxTokens?: number
  /** Maximum cumulative tokens (input + output) allowed for this run. */
  readonly maxTokenBudget?: number
  /** Optional context compression policy to control input growth across turns. */
  readonly contextStrategy?: ContextStrategy
  readonly temperature?: number
  /**
   * OpenAI-track sampling penalty for token frequency. Forwarded to OpenAI
   * cloud and OpenAI-compatible local servers (vLLM, llama-server). The
   * Anthropic adapter ignores this field.
   */
  readonly frequencyPenalty?: number
  /**
   * OpenAI-track sampling penalty for token presence. Forwarded to OpenAI
   * cloud and OpenAI-compatible local servers. The Anthropic adapter
   * ignores this field.
   */
  readonly presencePenalty?: number
  /**
   * Nucleus sampling cutoff. Forwarded to all adapters (OpenAI cloud,
   * OpenAI-compatible local, Anthropic).
   */
  readonly topP?: number
  /**
   * Top-k sampling cutoff. Forwarded to Anthropic and OpenAI-compatible
   * local servers. Cloud OpenAI rejects this parameter.
   */
  readonly topK?: number
  /**
   * Min-p sampling cutoff. Only supported by OpenAI-compatible local
   * servers (vLLM, llama-server, etc.). Cloud OpenAI rejects this parameter
   * and the Anthropic adapter ignores it.
   */
  readonly minP?: number
  /**
   * Whether the model may emit multiple tool calls in a single assistant
   * turn. Forwarded to OpenAI cloud and OpenAI-compatible local servers as
   * `parallel_tool_calls`. The Anthropic adapter ignores this field.
   *
   * - `undefined` (default): omit the field, server default applies. Cloud
   *   OpenAI defaults to `true`.
   * - `false`: force serial tool calling. Required by some local servers
   *   (vLLM, llama-server, certain quantized setups) that truncate
   *   concurrent `tool_call` deltas during streaming or return malformed
   *   `tool_calls` when more than one is emitted.
   * - `true`: explicit opt-in (matches the cloud default; no behavior
   *   change for cloud OpenAI users).
   */
  readonly parallelToolCalls?: boolean
  /**
   * Adapter-specific escape hatch merged into the outgoing request payload.
   * Spread between the sampling params and the structural fields, so values
   * here can override the standard sampling defaults (`temperature`, `topP`,
   * etc.) but cannot override transport-level fields (`model`, `messages`,
   * `tools`, `stream`, and Anthropic's `system`).
   */
  readonly extraBody?: Record<string, unknown>
  /**
   * Extended thinking / reasoning configuration. Provider mapping:
   * - Anthropic: forwards `enabled` + `budgetTokens` as the `thinking` request
   *   param (see `toAnthropicThinkingParam`).
   * - Gemini: forwards `enabled` + `budgetTokens` as `thinkingConfig`
   *   (`includeThoughts` defaults on when enabled).
   * - OpenAI: forwards `effort` as `reasoning_effort` (o-series, gpt-5).
   *   The `enabled`/`budgetTokens` fields are ignored ‚Äî OpenAI's reasoning
   *   surface is qualitative (`effort`), not quantitative.
   * - All other providers (Bedrock, local servers, etc.): ignored. Use
   *   {@link extraBody} for provider-specific reasoning controls instead.
   */
  readonly thinking?: ThinkingConfig
  /**
   * Maximum wall-clock time (in milliseconds) for the entire agent run.
   * When exceeded, the run is aborted via `AbortSignal.timeout()`.
   * Useful for local models where inference can be unpredictably slow.
   */
  readonly timeoutMs?: number
  /**
   * Maximum wall-clock time (in milliseconds) for a **single** LLM call
   * (one `adapter.chat()` request), re-armed fresh for every model call the
   * agent makes. Unset (the default) leaves each call bound only by the vendor
   * SDK's own request timeout, which is inconsistent across providers and
   * absent for some (a stalled provider can then hang the whole run).
   *
   * When set, OMA merges an `AbortSignal.timeout()` into each call so the bound
   * is uniform across every adapter and composes with {@link timeoutMs} (the
   * whole-run bound) ‚Äî whichever fires first wins. A per-call timeout surfaces
   * as an {@link LLMCallTimeoutError}, distinct from a caller `abortSignal`
   * cancellation. Because OMA calls the model non-streaming internally, this is
   * a wall-clock deadline over the entire response, so keep it generous for
   * slow local models or large reasoning outputs.
   */
  readonly callTimeoutMs?: number
  /**
   * Loop detection configuration. When set, the agent tracks repeated tool
   * calls and text outputs to detect stuck loops before `maxTurns` is reached.
   */
  readonly loopDetection?: LoopDetectionConfig
  /**
   * Maximum tool output length in characters for all tools used by this agent.
   * When set, tool outputs exceeding this limit are truncated (head + tail
   * with a marker in between). Per-tool {@link ToolDefinition.maxOutputChars}
   * takes priority over this value.
   */
  readonly maxToolOutputChars?: number
  /**
   * Compress tool results that the agent has already processed.
   *
   * In multi-turn runs, tool results persist in the conversation even after the
   * agent has acted on them. When enabled, consumed tool results (those followed
   * by an assistant response) are replaced with a short marker before the next
   * LLM call, freeing context budget for new reasoning.
   *
   * - `true` ‚Äî enable with default threshold (500 chars)
   * - `{ minChars: N }` ‚Äî only compress results longer than N characters
   * - `false` / `undefined` ‚Äî disabled (default)
   *
   * Error tool results are never compressed.
   */
  readonly compressToolResults?: boolean | { readonly minChars?: number }
  /**
   * Opt-in: when an outbound IR-to-native conversion encounters a
   * {@link ReasoningBlock} that the target adapter cannot natively echo
   * (see {@link LLMAdapter.capabilities.echoesReasoning}), downgrade the
   * block to `<thinking>...</thinking>` text and prepend to the next text
   * part instead of dropping it silently (today's default).
   *
   * Triggers in two scenarios:
   *  1. **Same-provider replay** through a `'never'` adapter (e.g. an
   *     OpenAI o-series response carries `reasoning_content` that gets
   *     extracted to a `ReasoningBlock`; on the next turn it would
   *     normally be dropped because Chat Completions doesn't accept
   *     reasoning input).
   *  2. **Cross-provider handoff** ‚Äî `Agent.prompt()` switching `model`
   *     mid-conversation, or any other path that puts a block from one
   *     provider in front of another adapter.
   *
   * Default `false` because enabling silently inflates prompt tokens
   * (reasoning blocks can be very long) and emits the model's internal
   * thinking text to potentially different providers, both of which the
   * user should consent to. See #223 for the design discussion.
   *
   * Caveat: some OpenAI-compatible local models echo `<thinking>` text
   * back as if it were instruction, which can trip the loop detector ‚Äî
   * see `examples/patterns/cross-provider-reasoning.ts`.
   */
  readonly preserveReasoningAsText?: boolean
  /**
   * Truncate the inner text of each `<thinking>` block emitted by the
   * {@link preserveReasoningAsText} fallback. Shares the
   * `boolean | { minChars?: number }` shape with
   * {@link compressToolResults}, but **the compression method differs**:
   *
   *   - `compressToolResults`: blocks over the threshold are *replaced*
   *     wholesale with a short marker string.
   *   - `compressReasoningText`: blocks over the threshold are kept but
   *     reduced to a head+tail excerpt that fits within `minChars`. The
   *     same number serves as both the activation threshold AND the cap on
   *     the post-truncation length.
   *
   * Values:
   * - `true` ‚Äî enable with default 1200-char head+tail excerpt.
   * - `{ minChars: N }` ‚Äî pass blocks of length ‚â§ N untouched; truncate
   *   longer blocks to fit within N chars.
   * - `false` ‚Äî never truncate (footgun for long CoT, not recommended).
   * - `undefined` ‚Äî defaults to `true` when {@link preserveReasoningAsText}
   *   is `true`, else `false`. Matches the maintainer's guidance in #223
   *   to "tie compress default-on to the preserve flag".
   *
   * Has no effect when {@link preserveReasoningAsText} is `false` (no
   * fallback runs, so nothing to truncate).
   */
  readonly compressReasoningText?: boolean | { readonly minChars?: number }
  /**
   * Optional Zod schema for structured output.  When set, the agent's final
   * output is parsed as JSON and validated against this schema.  A single
   * retry with error feedback is attempted on validation failure.
   *
   * **Distinct from {@link ToolDefinition.outputSchema}**, which validates an
   * individual tool's `ToolResult.data` string. This one operates on the
   * agent's final answer as parsed JSON.
   */
  readonly outputSchema?: ZodSchema
  /**
   * Called before each agent run. Receives the prompt and agent config.
   * Return a (possibly modified) context to continue, or throw to abort the run.
   * Only `prompt` from the returned context is applied; `agent` is read-only informational.
   */
  readonly beforeRun?: (context: BeforeRunHookContext) => Promise<BeforeRunHookContext> | BeforeRunHookContext
  /**
   * Called after each agent run completes successfully. Receives the run result.
   * Return a (possibly modified) result, or throw to mark the run as failed.
   * Not called when the run throws. For error observation, handle errors at the call site.
   */
  readonly afterRun?: (result: AgentRunResult) => Promise<AgentRunResult> | AgentRunResult
}

